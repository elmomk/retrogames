pub fn drawText(
    renderer: *sdl.Renderer,
    text: []const u8,
    x: i32,
    y: i32,
    scale: i32,
    r: u8,
    g: u8,
    b: u8,
    a: u8,
) void {
    var cx: i32 = x;
    for (text) |ch| {
        if (ch < 32 or ch > 127) {
            cx += 8 * scale;
            continue;
        }
        const idx = ch - 32;
        const glyph = FONT_DATA[idx];
        for (glyph, 0..) |row, gy| {
            var bit: u8 = 0;
            while (bit < 8) : (bit += 1) {
                if (row & (@as(u8, 0x80) >> @as(u3, @intCast(bit))) != 0) {
                    sdl.fillRect(
                        renderer,
                        cx + @as(i32, @intCast(bit)) * scale,
                        y + @as(i32, @intCast(gy)) * scale,
                        scale,
                        scale,
                        r, g, b, a,
                    );
                }
            }
        }
        cx += (8 + 1) * scale;
    }
}

pub fn measureText(text: []const u8, scale: i32) i32 {
    return @as(i32, @intCast(text.len)) * (8 + 1) * scale;
}
